    /// <summary>
    ///  Borra el código del if-else y sustitúyelo por uno que use el operador ternario.
    /// </summary>
    public class BNumeroMayorTernario
    {
        public int DeterminarNumeroMayor(int a, int b)
        {
            return a > b ? a : b;
        }
    }
